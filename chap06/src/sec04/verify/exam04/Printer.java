package sec04.verify.exam04;

public class Printer{
	void println(int message){
		System.out.println(message);
	}
	void println(boolean message){
		System.out.println(message);
	}
	void println(double message){
		System.out.println(message);
	}
	void println(String message){
		System.out.println(message);
	}
}